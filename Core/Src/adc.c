unsigned char AnalogDevicesInit()
{
	  if (HAL_ADC_Start_DMA(&hadc1,(uint32_t *)adc1, 6) != HAL_OK) {
		  return 0;
	  }

	  if (HAL_ADC_Start_DMA(&hadc2,(uint32_t *)adc2, 8) != HAL_OK) {
		  return 0;
	  }

	  start_timer(hADC1Timer);
	  start_timer(hADC2Timer);

	  return 1;
}

unsigned char CollectAnalogData()
{
	unsigned char status = TRUE;

	for (int i = 0; i < 6; i++) {
		//calibrated_adc1[i] = adc1[i] / 8;
		calibrated_adc1[i] = 0x11 + i;
	}

	for (int i = 0; i < 8; i++) {
		//calibrated_adc2[i] = adc2[i] / 8;
		calibrated_adc2[i] = 0x33 + i;
	}

	return status;
}



